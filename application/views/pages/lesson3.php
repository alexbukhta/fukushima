        <h2 class="featurette-heading">What do the Different Units Measure?</h2>
        <br/>
        <h3>Radioactivity: Becquerel and Curie</h3>
        <br/>
        <p>The Becquerel (Bq) and Curie (Ci) are measures of radioactivity - they measure the number of radioactive decays per unit time. Strictly speaking, one Becquerel is the activity of a lump of radioactive material in which one unstable atomic nucleus decays each second. The units are named after Henri Becquerel, Marie Curie, and Pierre Curie who shared the 1903 Nobel Prize in Physics for their discovery of radioactivity.</p>
        <br/>
        <p>1 Curie (Ci) = 37 Gigabecquerels (GBq)</p>
		<p>1 Becquerel (Bq) =  2.7*10^-11 Curies (Ci)</p>
        <br/>
        <p>You can skip the details and convert between the units with our handy converter. (Link to converter or embed converter (Bq to Ci only) for convenience)</p>
        <br/>
        <p>Most of the time the radioactivity data will be provided as Becquerels per unit mass. For example, the Japanese limits on radioactivity in fish are 100 Bq/kg. This number states that each kilogram of fish can contain unstable elements that decay up to a total of 100 times per second. </p>
        <br/>
        <p><b>Not all Becquerels are equal. The becquerel states how many nuclear decays we are likely to see per second It does not say anything about the type and energy of radiation emitted by the atomic decay. </b></p>
        <hr/>
        <h3>Absorbed Dose: Rad and Gray</h3>
        <br/>
        <p>The Rad (rad) and Grey (Gy) are measures of absorbed dose - they measure the amount of energy deposited by radiation per unit mass. In order to make this measurement seem more familiar, think of it this way: if the sunâ€™s rays represent radioactivity, the Rad and Grey tell us how well the sun will warm us on a given day. </p>
        <br/>
        <p>1 Gray = 100 Rads</p>
        <br/>
        <p>While absorbed dose comes closer to expressing the true effects of radiation, it falls short in that it does not account for the different effects that the different types of radiation have on the human body. <b>Although 1 Gray of gamma radiation deposits the same amount of energy into human tissue as 1 Gray of alpha radiation, the two types of radiation have a different biological impact.</b> The Sievert and Rem solve this problem and are described below. </p>
        <hr/>
        <h3>Effective Dose: Sievert and Rem</h3>
        <br/>
        <p>The Sievert (Sv) and Roentgen Equivalent Man (Rem) are measures of effective dose - they express the biological impact of radiation in units of energy per unit mass. Although the units of absorbed dose and effective dose are the same, both are measures of energy per unit mass, the effective dose accounts for the relative biological impact of the different types of radiation. One can go from absorbed dose to effective dose by multiplying the absorbed dose by the appropriate weighting factor.</p>
